
function verificarProcesador(){
    if (document.getElementById("amd-seleccion").values=true){

        botonSeleccionado = document.getElementById('amd-seleccion');
        miVariable = botonSeleccionado.dataset.valor;
        document.getElementById("mensaje").innerHTML=miVariable // Imprime "valor personalizado"
        
    }
}

